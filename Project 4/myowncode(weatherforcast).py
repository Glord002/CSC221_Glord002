import requests

def fetch_weather_forecast(api_key, city, country_code):
    """Fetch weather forecast data from OpenWeatherMap API."""
    url = f"http://api.openweathermap.org/data/2.5/forecast?q={city},{country_code}&appid={api_key}&units=metric"
    response = requests.get(url)
    weather_forecast = response.json()
    return weather_forecast

def display_weather_forecast(weather_forecast):
    """Display weather forecast data."""
    print("Weather Forecast:")
    for forecast in weather_forecast['list']:
        date_time = forecast['dt_txt']
        temperature = forecast['main']['temp']
        weather_description = forecast['weather'][0]['description']
        print(f"Date/Time: {date_time}, Temperature: {temperature}Â°C, Weather: {weather_description}")

